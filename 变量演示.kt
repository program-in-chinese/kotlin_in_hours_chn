    val 不认识人数: Int = 1234567890; // 整数: -2^31到(2^31-1)
    val 星球数: Long = 123456789000000000L; // 长整数: -2^63到(2^63-1)
    val 身高: Float = 1100000000f; // 单精度浮点数: 2^-126到(2-2^-23)*2^127
    val 体重: Double = 999999999999.9; // 双精度浮点数: 2^-1074到(2-2^-52)*2^1023
}